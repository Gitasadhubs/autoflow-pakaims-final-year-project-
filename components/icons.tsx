export const StatusIcon: React.FC<{ status: 'queued' | 'in_progress' | 'completed' | null, conclusion: string | null, className?: string }> = ({ status, conclusion, className = 'h-5 w-5' }) => {
    if (status === 'queued') {
        return <div className={`rounded-full bg-yellow-500 ${className}`} title="Queued"></div>;
    }
    if (status === 'in_progress') {
        return <Spinner className={`${className} text-blue-500`} title="In Progress" />;
    }
    if (status === 'completed') {
        if (conclusion === 'success') {
            return (
                <svg className={`${className} text-green-500`} fill="none" viewBox="0 0 24 24" stroke="currentColor">
                    <title>Success</title>
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
                </svg>
            );
        }
        if (conclusion === 'failure' || conclusion === 'cancelled' || conclusion === 'timed_out') {
            return (
                <svg className={`${className} text-red-500`} fill="none" viewBox="0 0 24 24" stroke="currentColor">
                    <title>Failed</title>
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M10 14l2-2m0 0l2-2m-2 2l-2-2m2 2l2 2m7-2a9 9 0 11-18 0 9 9 0 0118 0z" />
                </svg>
            );
        }
         // Handle other conclusions like skipped, neutral
        return (
            <svg className={`${className} text-gray-500`} fill="none" viewBox="0 0 24 24" stroke="currentColor">
                <title>Completed: {conclusion}</title>
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z" />
            </svg>
        );
    }
    
    // If status is null, there are no runs.
    if (status === null) {
      return (
        <svg className={`${className} text-gray-400`} fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <title>No recent runs</title>
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 12H9m12 0a9 9 0 11-18 0 9 9 0 0118 0z" />
        </svg>
      );
    }
    
    return <div className={`rounded-full bg-gray-500 ${className}`} title="Unknown"></div>;
};


